#include <stdio.h>

int main()
{
    int a = 4;
    printf("Integer value given %d \n", a);
    float b = 7.88;
    printf("Float value given %f \n", b);
    printf("%d %d %d %f %f /n", a, a, a, b, b); // We can also write as such
    char ch = 'g';
    printf("Character given : %c \n", ch);

    return 0;
}